// Fonction utilitaire pour obtenir des questions par catégorie
export function getQuestionsByCategory(category: string): Question[] {
  return QUESTIONS.filter(q => q.category === category)
}

// Fonction pour générer un examen aléatoire
export function generateExam(count: number = 30, categories?: string[]): Question[] {
  let pool = categories
    ? QUESTIONS.filter(q => categories.includes(q.category))
    : [...QUESTIONS]
  return pool.sort(() => Math.random() - 0.5).slice(0, count)
}
